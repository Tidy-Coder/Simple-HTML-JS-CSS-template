function retablirCouleurGratuit(){
  document.getElementById("cestgratuit").style.color = "#aaa";
  document.getElementById("cestgratuit").style.background = "#111";
  document.getElementById("cestgratuit").style.scale = "1";
  document.getElementById("cestgratuit").style.left = "0";
}
function changerCouleurGratuit(){
  document.getElementById("cestgratuit").style.color = "#111";
  document.getElementById("cestgratuit").style.background = "orange";
  document.getElementById("cestgratuit").style.scale = "1.01";
  document.getElementById("cestgratuit").style.left = "1em";
}
